    private void GuardarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_GuardarActionPerformed
        EntidadDesarrolladora desarrolladora=new EntidadDesarrolladora(0,jTextNombre.getText() );
        CRUD_Desarrolladora crud=new CRUD_Desarrolladora();
        boolean incertoDato=crud.incertar(desarrolladora);
        
        if(incertoDato=true){
            jLabelAnuncio.setVisible(true);
        }else{
            jLabelAnuncio.setText("ocurrio un error");
            jLabelAnuncio.setVisible(true);
        }
        rellenarComboBox();
        jTextNombre.setText("");
    }//GEN-LAST:event_GuardarActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       
        String cadena=jComboBoxID.getSelectedItem().toString();
        String[] separarnumero=cadena.split(" ");
        int numero=Integer.parseInt(separarnumero[0]);
        
        CRUD_Desarrolladora crud=new CRUD_Desarrolladora();
        EntidadDesarrolladora eDesarrolladora=new EntidadDesarrolladora(numero, separarnumero[1]);
        
        boolean elimino=crud.eliminar(eDesarrolladora);
        
        if(elimino=true){
            jLabelAnuncio.setText("se elimino correctamente");
            jLabelAnuncio.setVisible(true);
        }
        rellenarComboBox();
        
    }//GEN-LAST:event_jButton2ActionPerformed

