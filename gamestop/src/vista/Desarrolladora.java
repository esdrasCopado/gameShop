        public void rellenarComboBox(){
        Conexion conexion=new Conexion();
        Statement statement;
        ResultSet resulset;
        jComboBoxID.removeAllItems();
        try {
            statement=conexion.getConexionBaseDatos().createStatement();
            resulset=statement.executeQuery("SELECT * FROM desarrolladora");
            jComboBoxID.addItem("Nuevo elemento");
            while(resulset.next()){
               jComboBoxID.addItem(resulset.getInt("id")+" "+resulset.getString("nombre")); 
            }
        } catch (Exception e) {
            System.out.println("error"+" "+e);
        }
        
    }

